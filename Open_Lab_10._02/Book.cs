using System;
using System.Collections.Generic;
using System.Text;

namespace Open_Lab_10._02
{
    class Book
    {
        private string title;
        private int pages;
        private string category;
        private string author;
        private string releaseDate;
        public string _title
        {
            get
            {
                return _title;
            }
            set
            {
                _title = title;
            }
        }
        public int _pages
           {
            get
            {
                return _pages;
            }
    set
            {
                _pages = pages;
            }
        }
        public string _category
{
    get
            {
        return _category;
    }
    set
            {
        _category = category;
    }
}
public string _author
{
    get
            {
        return _author;
    }
    set
            {
        _author = author;
    }
}
public string _releaseDate
{
    get
            {
        return _releaseDate;
    }
    set
            {
        _releaseDate = releaseDate;
    }
}


    }
}
